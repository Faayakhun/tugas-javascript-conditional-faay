let move = "UP";

switch(move) {
    case "UP":
        console.log("Karakter bejalan keatas");
        break;
    case "RIGHT" :
        console.log("Karakter berjalan kekanan");
        break;
    case "BOTTOM" :
        console.log("Karakter berjalan kebawah");
        break;
    case "LEFT" :
        console.log("Karakter berjalan kekiri");
        break;
    default :
        console.log("Masukan arah yang benar")
    

}